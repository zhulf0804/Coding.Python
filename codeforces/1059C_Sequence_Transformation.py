if n == 1:
    print(1)
elif n == 2:
    print(1, 2)
elif n == 3:
    print(1, 1, 3)
else:
    res = []
    i = 0
    while n != 2 and n != 3:
        num = (n + 1) // 2
        res.extend([pow(2, i)] * num)
        i += 1
        n -= num

    if n == 2:
        res.append(pow(2, i))
        res.append(pow(2, i + 1))
    elif n == 3:
        res.extend([pow(2, i)] * 2)
        res.append(pow(2, i) * 3)
    res = list(map(str, res))
    print(' '.join(res))